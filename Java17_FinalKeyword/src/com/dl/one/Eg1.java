package com.dl.one;

public class Eg1 {
	
	//Final Keyword for variables
	
	final int a=20;
	

	public static void main(String[] args) {
		 System.out.println(new Eg1().a);

	}

}
//KeyPoint
//The final keyword in java is used to restrict the user. The java final keyword can be used with variables, methods and classes.
//The variable declared with final cannot be reassign
//If we declare final inside the class without any method then the object is created similar to instance variable .

