package com.dl.three;

//Operator overloading

public class Eg1 {

	public static void main(String[] args) {
		
		//add 
		int a =10;
		int b=20;
		System.out.println(a+b);
		
		
		//Concatenation 
		
		String s1= "Lakshmi";
		String s2 = "Sravanthi";
		System.out.println(s1+" " +s2);

	}

}


//KeyPoint 

//Operator overloading is a technique which is used to perform the operator based on the user defined types
//Here in example the (+) operator is used to add numbers and in second example it is used to contact the string.