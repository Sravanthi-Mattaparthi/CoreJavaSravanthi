public class Eg4 {

	public static void main(String[] args) {
		// Ternary operator
		
		int a, b;
		a=10;
		b= (a==1)? 10:20;
		System.out.println(b);
		
		
		b= (a==10)? 10:20;
		System.out.println(b);

	}

}
