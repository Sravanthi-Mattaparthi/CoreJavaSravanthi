package com.dl.one;

public class Eg6 {

	public static void main(String[] args) {
		// Logical operators
		//&& both the conditions should satisfy
		
		System.out.println(true && true);
		System.out.println(false && true);
		System.out.println(true && false);
		System.out.println(false && false);
		System.out.println("Above is the output for logical AND");
		System.out.println("");
		
		
		
		System.out.println(true || true);
		System.out.println(false || true);
		System.out.println(true || false);
		System.out.println(false || false);
		System.out.println("Above is the output for logical OR");
		System.out.println("");
		
		//Bitwise Operators Exclusive
		System.out.println(true ^ true);
		System.out.println(false ^ true);
		System.out.println(true ^ false);
		System.out.println(false ^ false);
		System.out.println("Above is the output for logical Bitwise Exclusive");
		System.out.println("");
		
		//Bitwise Operators AND
		
		System.out.println(true & true);
		System.out.println(false & true);
		System.out.println(true & false);
		System.out.println(false & false);
		System.out.println("Above is the output for logical Bitwise AND");
		System.out.println("");
		
		//Bitwise Operators OR
		
		System.out.println(true | true);
		System.out.println(false | true);
		System.out.println(true | false);
		System.out.println(false | false);
		System.out.println("Above is the output for logical Bitwise OR");
		System.out.println("");
		
	}

}
