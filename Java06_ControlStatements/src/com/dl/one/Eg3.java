public class Eg3 {

	public static void main(String[] args) {
		// else if 
		
		double userSalary = 50000.0d;
		
		if(userSalary ==10000.0) {
			System.out.println("userSalary:" + userSalary);
		}
		else if (userSalary ==20000.0) {
			System.out.println("userSalary:" + userSalary);	
		}
		else if (userSalary ==30000.0) {
			System.out.println("userSalary:" + userSalary);	
		}
		else if (userSalary ==40000.0) {
			System.out.println("userSalary:" + userSalary);	
		}
		else {
			System.out.println("Salary out of range ");	
		}	

	}

}
