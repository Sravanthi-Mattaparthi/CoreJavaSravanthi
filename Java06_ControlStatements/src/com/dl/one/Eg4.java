package com.dl.one;

public class Eg4 {

	public static void main(String[] args) {
		// Switch Case
		int salary = 10000;
		
		switch (salary){
		case 10000:
			System.out.println("userSalary:" + salary);
			break;
			
		case 20000:
			System.out.println("userSalary:" + salary);
			break;
		
		case 30000:
			System.out.println("userSalary:" + salary);
			break;
			
		case 40000:
			System.out.println("userSalary:" + salary);
			break;
			
		default:
			System.out.println("Salary is out of range");
			break;
		}

	}

}
