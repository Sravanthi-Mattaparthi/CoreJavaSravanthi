package com.dl.two;

public class Eg2 {

	public static void main(String[] args) {
		//While loop
	
		
		int i =0;
		while(i<=10) {
			System.out.println(i);
			i++;
		}
		int j=10;
		while(j>=0) {
			System.out.println(j);
			j--;
		}
		
	}
}


//KeyPoint
// When we don't know number of iteration in advance we use "While loop"