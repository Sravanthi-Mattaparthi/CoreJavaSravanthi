package com.dl.two;

public class Eg1 {
	
	public static void main(String args[]) {
		// For loop	
	
			for(int i =0; i<=10; i++) {
				System.out.println(i);
			}
			System.out.println("");
			for(int j =10; j>=0; j--) {
				System.out.println(j);
			}
		}
}
//KeyPoint
//When we know the number of conditions then we can use "FOR" loop
